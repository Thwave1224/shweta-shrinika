    <script>
    function validateForm() {
        // Clear any existing error messages
        document.getElementById("fname-error").classList.remove("visible");
        document.getElementById("pincode-error").classList.remove("visible");
        document.getElementById("houseNo-error").classList.remove("visible");
        document.getElementById("address-error").classList.remove("visible");

        var fname = document.getElementById("fname").value.trim();
        var pincode = document.getElementById("pincode").value.trim();
        var houseNo = document.getElementById("houseNo").value.trim();
        var address = document.getElementById("address").value.trim();
        var isValid = true;

        if (fname === "") {
            document.getElementById("fname-error").classList.add("visible");
            isValid = false;
        }
        if (pincode === "" || !/^\d{6}$/.test(pincode)) {
            document.getElementById("pincode-error").classList.add("visible");
            isValid = false;
        }
        if (houseNo === "") {
            document.getElementById("houseNo-error").classList.add("visible");
            document.getElementById("houseNo-error").textContent = "Please enter your house number";
            isValid = false;
        }
        if (address === "") {
            document.getElementById("address-error").classList.add("visible");
            isValid = false;
        }

        // If all validations pass
        if (isValid) {
            window.location.href = "payment.php";
        }
    }
    </script>
